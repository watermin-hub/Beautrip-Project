export function LanguageProvider({ children }: { children: ReactNode }) {
  // ì´ˆê¸°ê°’ì€ í•­ìƒ "KR"ë¡œ ì„¤ì •í•˜ì—¬ ì„œë²„/í´ë¼ì´ì–¸íŠ¸ ì¼ì¹˜ ë³´ì¥
  const [language, setLanguageState] = useState<LanguageCode>("KR");
  const [isMounted, setIsMounted] = useState(false);

  // í´ë¼ì´ì–¸íŠ¸ì—ì„œë§Œ localStorage ì½ê¸°
  useEffect(() => {
    setIsMounted(true);
    if (typeof window !== "undefined") {
      const saved = localStorage.getItem("language") as LanguageCode;
      if (
        saved &&
        (saved === "KR" || saved === "EN" || saved === "JP" || saved === "CN")
      ) {
        setLanguageState(saved);
      }
    }
  }, []);

  useEffect(() => {
    if (isMounted && typeof window !== "undefined") {
      localStorage.setItem("language", language);
      // ì–¸ì–´ ë³€ê²½ ì´ë²¤íŠ¸ ë°œìƒ
      window.dispatchEvent(
        new CustomEvent("languageChanged", { detail: language })
      );
    }
  }, [language, isMounted]);

  const setLanguage = (lang: LanguageCode) => {
    setLanguageState(lang);
    // GTM: ì–¸ì–´ ë³€ê²½ ì´ë²¤íŠ¸
    if (typeof window !== "undefined") {
      const { trackLanguageChange } = require("@/lib/gtm");
      trackLanguageChange(lang);
    }
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    const langTranslations = translations[language];
    if (!langTranslations) {
      console.error(
        `[Translation] Language "${language}" not found in translations`
      );
      return key;
    }
    const translation = langTranslations[key];
    if (!translation) {
      console.warn(
        `[Translation] Key "${key}" not found for language "${language}"`
      );
      return key;
    }

    // íŒŒë¼ë¯¸í„°ê°€ ìˆìœ¼ë©´ ì¹˜í™˜
    if (params) {
      let result = translation;
      for (const [paramKey, paramValue] of Object.entries(params)) {
        result = result.replace(
          new RegExp(`\\{${paramKey}\\}`, "g"),
          String(paramValue)
        );
      }
      return result;
    }

    return translation;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
